# LOOPS IN PYTHON 
# loops are used to repeat instruction

# WHILE 

count = 1
while count<=5:        # condition
    print("hello world")
    count += 1

print(count) #output --> 6




# FOR 
# loops are used for sequectial traversal . for traversing list,string,tuples etc.

list = [1,2,3,4,5]

for el in list:
    print(el)    # some work





