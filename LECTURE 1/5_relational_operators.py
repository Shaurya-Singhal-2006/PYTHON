# relational operators ( == , != , >= , > , <= , < )

a = 90 
b = 68

print(a == b )            # equals to ( answer is in terms of true or false )(boolean value)
print(a != b )            # not equals to ( answer is in terms of true or false )(boolean value)
print(a >= b )            # greater than equal to ( answer is in terms of true or false )(boolean value)
print( a > b )            # greater than ( answer is in terms of true or false )(boolean value)
print(a <= b )            # less than equal to ( answer is in terms of true or false )(boolean value)
print( a < b )            # less than ( answer is in terms of true or false )(boolean value)