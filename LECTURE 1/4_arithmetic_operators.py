# ARITHMETIC OPERATORS  ( + , - , * , / , % , ** )

a = 10
b = 40 

sum = a + b 
diff = b - a 
multi = a * b 
div = a / b 
rem = a % b 
pow = a ** b

print(sum)      #addition
print(diff)     #subtraction
print(multi)    #multiplication
print(div)      #division                                 # the answer of division will always be a floating value           
print(rem)      #remainder 
print(pow)      #power e.g a^b