name3 = '''shaurya'''

print(name1)
print(name2)
print(name3)

# 3. float  --> decimal numbers (+ve , -ve)

b = 25.50
print(b)

# 4. boolen  --> true & false  (we write True & False with F and T capital)

old = False    
print(type(old))

# 5. none  --> where no value is stored

c = None
print(type(c))




